#!/usr/bin/env node
/**
 * Post-build SEO redirects:
 * - 301 redirect HTML for blog aliases, legacy /blog/, and /blogss/ paths
 * - CloudFront viewer-request function with host + path normalization
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import matter from 'gray-matter';
import { getSiteUrl } from './site-url.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const OUT = path.join(ROOT, 'out');
const BLOG_DIR = path.join(ROOT, 'content/blog');
const CF_DIR = path.join(ROOT, 'infra/cloudfront');
const SITE_URL = getSiteUrl();
const SITE_HOST = new URL(SITE_URL).host;
const BARE_HOST = SITE_HOST.replace(/^www\./, '');

/** slug → canonical slug (includes canonical slugs and all aliases). */
function getAllBlogSlugMappings() {
  const mappings = new Map();

  for (const file of fs.readdirSync(BLOG_DIR).filter((name) => name.endsWith('.mdx'))) {
    const raw = fs.readFileSync(path.join(BLOG_DIR, file), 'utf8');
    const { data } = matter(raw);
    const canonical = String(data.slug || file.replace(/\.mdx$/, ''));
    mappings.set(canonical, canonical);
    const aliases = Array.isArray(data.aliases) ? data.aliases.map(String) : [];
    for (const alias of aliases) {
      mappings.set(alias, canonical);
    }
  }

  return [...mappings.entries()].map(([slug, canonical]) => ({ slug, canonical }));
}

function redirectHtml(targetUrl) {
  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="robots" content="noindex, follow">
  <link rel="canonical" href="${targetUrl}">
  <meta http-equiv="refresh" content="0;url=${targetUrl}">
  <title>Redirecting…</title>
  <script>location.replace(${JSON.stringify(targetUrl)});</script>
</head>
<body>
  <p>Redirecting to <a href="${targetUrl}">${targetUrl}</a>…</p>
</body>
</html>`;
}

function writeRedirectHtml(relativePath, targetUrl) {
  const outPath = path.join(OUT, relativePath);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, redirectHtml(targetUrl));
}

function buildBlogRedirectMap(slugMappings) {
  const blogRedirectMap = {};

  for (const { slug, canonical } of slugMappings) {
    blogRedirectMap[`/blogs/${slug}`] = `/blogs/${canonical}`;
    blogRedirectMap[`/blogs/${slug}.html`] = `/blogs/${canonical}`;
    blogRedirectMap[`/blog/${slug}`] = `/blogs/${canonical}`;
    blogRedirectMap[`/blog/${slug}.html`] = `/blogs/${canonical}`;
    blogRedirectMap[`/blogss/${slug}`] = `/blogs/${canonical}`;
    blogRedirectMap[`/blogss/${slug}.html`] = `/blogs/${canonical}`;
  }

  return blogRedirectMap;
}

function buildCloudFrontFunction(blogRedirectMap) {
  const mapJson = JSON.stringify(blogRedirectMap, null, 2);
  return `// Generated by scripts/generate-seo-redirects.mjs — do not edit manually.
function handler(event) {
  var request = event.request;
  var headers = request.headers;
  var host = headers.host ? headers.host.value : '';
  var uri = request.uri;
  var query = '';

  if (request.querystring && Object.keys(request.querystring).length > 0) {
    var parts = [];
    for (var key in request.querystring) {
      var entry = request.querystring[key];
      if (entry.multiValue) {
        for (var i = 0; i < entry.multiValue.length; i++) {
          parts.push(key + '=' + entry.multiValue[i].value);
        }
      } else if (entry.value) {
        parts.push(key + '=' + entry.value);
      }
    }
    if (parts.length > 0) query = '?' + parts.join('&');
  }

  function redirect301(location) {
    return {
      statusCode: 301,
      statusDescription: 'Moved Permanently',
      headers: { location: { value: location } }
    };
  }

  function resolveBlogTarget(prefix, rawPath) {
    var slug = rawPath.slice(prefix.length).replace(/\\.html$/, '').replace(/\\/$/, '');
    if (!slug) return '/blogs';
    var mapped = blogRedirects['/blogs/' + slug];
    return mapped || '/blogs/' + slug;
  }

  var canonicalOrigin = '${SITE_URL}';
  var bareHost = '${BARE_HOST}';
  var blogRedirects = ${mapJson};

  if (host === bareHost) {
    return redirect301(canonicalOrigin + uri + query);
  }

  if (uri === '/blog' || uri === '/blog/' || uri === '/blog.html') {
    return redirect301(canonicalOrigin + '/blogs' + query);
  }

  if (uri === '/blogss' || uri === '/blogss/' || uri === '/blogss.html') {
    return redirect301(canonicalOrigin + '/blogs' + query);
  }

  if (uri.indexOf('/blog/') === 0) {
    return redirect301(canonicalOrigin + resolveBlogTarget('/blog/', uri) + query);
  }

  if (uri.indexOf('/blogss/') === 0) {
    return redirect301(canonicalOrigin + resolveBlogTarget('/blogss/', uri) + query);
  }

  if (uri.endsWith('.html') && uri !== '/index.html') {
    var clean = uri.slice(0, -5);
    if (clean === '/index') clean = '/';
    return redirect301(canonicalOrigin + clean + query);
  }

  if (uri.length > 1 && uri.endsWith('/')) {
    return redirect301(canonicalOrigin + uri.slice(0, -1) + query);
  }

  if (blogRedirects[uri]) {
    return redirect301(canonicalOrigin + blogRedirects[uri] + query);
  }

  return request;
}
`;
}

function main() {
  if (!fs.existsSync(OUT)) {
    console.error('out/ not found - run next build first');
    process.exit(1);
  }

  const slugMappings = getAllBlogSlugMappings();
  const blogRedirectMap = buildBlogRedirectMap(slugMappings);
  let staticRedirectCount = 0;

  for (const { slug, canonical } of slugMappings) {
    const targetUrl = `${SITE_URL}/blogs/${canonical}`;

    writeRedirectHtml(`blog/${slug}.html`, targetUrl);
    writeRedirectHtml(`blogss/${slug}.html`, targetUrl);
    staticRedirectCount += 2;

    if (slug !== canonical) {
      writeRedirectHtml(`blogs/${slug}.html`, targetUrl);
      staticRedirectCount += 1;
    }
  }

  writeRedirectHtml('blog.html', `${SITE_URL}/blogs`);
  writeRedirectHtml('blogss.html', `${SITE_URL}/blogs`);
  staticRedirectCount += 2;

  fs.mkdirSync(CF_DIR, { recursive: true });
  const cfFunction = buildCloudFrontFunction(blogRedirectMap);
  fs.writeFileSync(path.join(CF_DIR, 'viewer-request.generated.js'), cfFunction);
  fs.writeFileSync(
    path.join(CF_DIR, 'redirect-map.json'),
    JSON.stringify(
      {
        siteUrl: SITE_URL,
        slugMappings,
        blogRedirects: blogRedirectMap,
      },
      null,
      2,
    ),
  );

  console.log(
    `Generated ${staticRedirectCount} legacy redirect pages (${slugMappings.length} slugs) and CloudFront function for ${SITE_URL}`,
  );
}

main();
